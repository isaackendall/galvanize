// 14 indexOf on an Array

// Complete the function indexOfArray. This function will take in two parameters, 
// an array and an element, and returns the index, inside of the array, where the 
// element is located. You will want to use the indexOf method for Arrays. 
// If the element is not present within array, your function should return -1.

function indexOfArray(array, item) {
  // your code here
  var index;
  index = array.indexOf(item);
  return index;
}